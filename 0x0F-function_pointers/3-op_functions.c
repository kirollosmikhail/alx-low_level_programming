#include "3-calc.h"

/**
* op_add - adds two numbers.
* @a: 1st num.
* @b: 2nd num.
*
* Return: The addition of the two numbers.
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
* op_sub - subtracts the first num the 2nd num.
* @a: 1st num.
* @b: 2nd num.
*
* Return: The subtraction of the two numbers.
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
* op_mul - multiplies two numbers.
* @a: 1st num.
* @b: 2nd num.
*
* Return: The multiplication of the two numbers.
*/

int op_mul(int a, int b)
{
	return (a * b);
}
/**
* op_div - divides two numbers.
* @a: 1st num.
* @b: 2nd num.
*
* Return: The division of the first num by the 2nd num.
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod - divides two numbers and returns the remainder of the division.
* @a: 1st num.
* @b: 2nd num.
*
* Return: The remainder of dividing the first num by the 2nd num.
*/

int op_mod(int a, int b)
{
	return (a % b);
}
